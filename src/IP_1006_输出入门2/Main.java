package IP_1006_输出入门2;

import java.util.Scanner;

public class Main {

    public static void main(String[] args) {

        Scanner cin = new Scanner(System.in);
        int length = cin.nextInt();

        for (int i = 0; i < length; i++){
            int a, b;

            a = cin.nextInt();
            b = cin.nextInt();

            System.out.println((a + b) );
        }



    }

}
