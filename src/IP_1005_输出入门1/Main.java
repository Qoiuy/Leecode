package IP_1005_输出入门1;

import java.util.Scanner;

public class Main {

    public static void main(String[] args) {

        Scanner cin = new Scanner(System.in);

        int a, b;

        while (cin.hasNext()) {
            a = cin.nextInt();
            b = cin.nextInt();

            System.out.println((a + b));
        }

    }

}
